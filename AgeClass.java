public class AgeClass {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// Age Lab: Average age
		
		// Stating and Assigning Variables 
		double left = 16.0; // left person age
		double right = 15.0; //right person age
		double avg = (right+left)/2; // average age 
		
		// Printing Average Age and Even or Odd(0 or 1) of Average Age
		System.out.println("Average Age: " + avg); // Print Average Age
		System.out.println("Average Age (Mod 2): " + avg%2); // Print 0 or 1		

	}

}
